    /**
     * Este boton guarda las modificaciones que se realizaron al actualizar un empleado
     * @param evt crea un evento al precionarlo 
     */
    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        for (int i = 0; i < controlador.cantidadEmpleados(); i++) {
            if(id.equals(controlador.listaDeEmpleados.get(i).getId())){
                controlador.listaDeEmpleados.get(i).setId(txtId.getText());
                controlador.listaDeEmpleados.get(i).setNombre(txtNombre.getText());
                controlador.listaDeEmpleados.get(i).setApellido(txtApellido.getText());
                controlador.listaDeEmpleados.get(i).setTelefono(txtTelefono.getText());
                controlador.listaDeEmpleados.get(i).setDireccion(txtDireccion.getText());
            }
        }
       txtId.setText("");
       txtNombre.setText("");
       txtApellido.setText("");
       txtTelefono.setText("");
       txtDireccion.setText("");
       
       mostrarEmpleados();
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed
