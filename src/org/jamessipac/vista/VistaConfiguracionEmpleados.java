    /**
     * Agrega los datos de un empleado, el cual se agregara en el ArrayList
     * @param evt crea un evento al presionarlo
     */
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        Empleado empleado=new Empleado(txtId.getText(), txtNombre.getText(), txtApellido.getText(), txtTelefono.getText(), txtDireccion.getText());
        controlador.crearEmpleado(empleado);
        
        mostrarEmpleados();        

        txtId.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        txtTelefono.setText("");
        txtDireccion.setText("");
    }//GEN-LAST:event_btnAgregarActionPerformed
    /**
     * Con este boton se elimina a un empleado contenido en el ArrayList
     * @param evt crea un evento al presionarlo 
     */
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        for (int i = 0; i < controlador.cantidadEmpleados(); i++) {
            if(id.equals(controlador.listaDeEmpleados.get(i).getId())){
                controlador.listaDeEmpleados.remove(i);
            }
            mostrarEmpleados();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
