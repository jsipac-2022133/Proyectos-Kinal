    /**
     * Con este metodo se muestran los datos en la tabla
     */
    public void mostrarRestaurantes(){
        String[][] vector=new String[controlador.cantidadRestaurantes()][5];
        for (int i = 0; i < controlador.cantidadRestaurantes(); i++) {
            vector[i][0]=controlador.listaDeRestaurantes.get(i).getId();
            vector[i][1]=controlador.listaDeRestaurantes.get(i).getCalle();
            vector[i][2]=controlador.listaDeRestaurantes.get(i).getZona();
            vector[i][3]=controlador.listaDeRestaurantes.get(i).getLugar();
            vector[i][4]=controlador.listaDeRestaurantes.get(i).getTelefono();
        }
        jTableRestaurante.setModel(new javax.swing.table.DefaultTableModel(vector, new String[]{"Id","Calle","Zona","Lugar","Teléfono"}));
    }
