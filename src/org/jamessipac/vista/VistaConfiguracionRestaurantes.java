    /**
     * Este boton guarda las modificaciones que se realizaron al actualizar un restaurante
     * @param evt crea un evento al presionarlo
     */
    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        for (int i = 0; i < controlador.cantidadRestaurantes(); i++) {
            if(id.equals(controlador.listaDeRestaurantes.get(i).getId())){
            controlador.listaDeRestaurantes.get(i).setId(txtId.getText());
            controlador.listaDeRestaurantes.get(i).setCalle(txtCalle.getText());
            controlador.listaDeRestaurantes.get(i).setZona(txtZona.getText());
            controlador.listaDeRestaurantes.get(i).setLugar(txtLugar.getText());
            controlador.listaDeRestaurantes.get(i).setTelefono(txtTelefono.getText()); 
            }
        }
       txtId.setText(id);
       txtCalle.setText(calle);
       txtZona.setText(zona);
       txtLugar.setText(lugar);
       txtTelefono.setText(telefono);
       
       mostrarRestaurantes();
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed
